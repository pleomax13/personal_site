    <h3>4 ЭКСПЕРИМЕНТАЛЬНАЯ ПРОВЕРКА</h3>
    <p>
      Некалиброванный альфа-источник ~ 1 кБк 243Am (наиболее интенсивные энергии альфа-частиц:
      5,32 МэВ - 11% - и 5,36 МэВ - 87% [9]), нанесенный на диск из нержавеющей стали,
      был помещен на расстоянии 5,0 мм от фотодиода внутри корпуса системы, при комнатном
      давлении и температуре. Выходной импульс от усилителя, описанного в этой статье,
      подавался непосредственно в многоканальный анализатор ORTEC Spectrum Ace
      без какого-либо дополнительного усиления или обработки.
    </p>
    <p>
      Чтобы проверить работу усилителя, эксперимент длился 54000 секунд
      (время работы в реальном времени), и полученный спектр представлен на рисунке 4.
      Наблюдаемое время простоя системы составило 0,07%. Полученный спектр был проанализирован
      с использованием программного обеспечения IDEFIX [10], в котором используется модель
      Гаусса с экспоненциальным хвостом и функцией ошибки для подгонки к каждому пику; была
      выбрана группа из двух пиков, относящихся к наиболее интенсивным альфа-излучениям от
      243 Ам, а результирующая полная ширина на половине максимума (FWHM) каждого из пиков
      была менее 6%, тогда как в более дорогих системах она может быть величиной 1-2%.
      Полученные результаты, однако, идеально подходят для требований нейтронного счетчика,
      основанного на обнаружении альфа-частиц в ядерной реакции 10B (n, α) 7 Li [7],
      так как в этом случае PIN-фотодиод будет работать в основном как счетчик альфа-частиц -
      он также может быть полезен для других менее требовательных устройств обнаружения
      альфа-частиц. Поскольку предлагаемое устройство не требует отклика энергии, калибровка
      энергии не была выполнена -
      при необходимости она должна быть выполнена на будущей стадии.
    </p>
    <p>
      Чтобы проверить подавление системы, было выполнено второе обнаружение без
      присутствия радиоактивного источника в течении 72000с
      (время работы в реальном времени). В полученном спектре не
      было никаких подсчетов, указывающих на отличное выделение альфа-частиц от фоновых
      β-частиц и рентгеновских и γ-лучей. Это также говорит о том, что
      порог шума системы, если этого требует другое устройство, может быть уменьшен
      без ущерба для производительности.
    </p>
