    <h3>ВЫВОДЫ</h3>
    <p>
      Была разработана система, состоящая из предусилителя и формирующего
      усилителя для использования с фотодиодом S3590-09 PIN с целью создания
      недорогой системы, которая может быть применена в альфа- или нейтронной системе
      обнаружения (с использованием соответствующего борного преобразователя).
      Производительность системы для обнаружения альфа-частиц была определена с
      использованием альфа-источника 243Am, что позволило получить энергетическое
      разрешение менее 6%. Кроме того, измерение фона не привело ни к каким подсчетам,
      показывая, что система отлично подходит для предполагаемого применения в качестве
      счетчика частиц. Весь альфа-спектрометр (исключая фотодиод) был собран менее чем за
      15 долларов США с использованием только общедоступных компонентов.
