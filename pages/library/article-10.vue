    <h3>1 ВВЕДЕНИЕ</h3>
    <p>
      Кремниевые PIN-фотодиоды обеспечивают надежное и компактное решение для
      радиационного контроля. Альфа, бета, гамма и рентгеновское излучение могут
      быть обнаружены либо непосредственно через поглощение в кристаллической решетке [1]
      или косвенно через измерение люминесцентного излучения сцинтилляционного кристалла [2].
    </p>
    <p>
      Есть несколько работ, в которых сообщается о применении
      PIN-фотодиодов для прямого обнаружения излучение с использованием
      дорогого малошумящего зарядного предусилителя [3–6]. В некоторых случаях
      вход окна на некоторых неинкапсулированных PIN-фотодиодах достаточно мал,
      чтобы позволить альфа-частицам проникнуть в область истощения, и некоторые модели с
      малой утечкой показали хорошую разрешающую способность, достаточную для подключения
      к недорогим предусилителям для альфа-излучения
      или нейтронного излучение (с использованием борного преобразователя).
    </p>
    <p>
      При разработке нейтронного детектора с использованием борного преобразователя и
      PIN-фотодиода [7], мы столкнулись с необходимостью недорогого портативного и
      универсального усилителя, который мог бы легко встраиваться в детектор. Как и
      в таком устройстве, детектор будет использоваться в качестве счетчика частиц,
      наиболее важной особенностью усилителя является не разрешающая способность,
      а шумоподавление - возможность выступать в качестве спектрометра будет полезна
      только во время этапа разработки,
      или для тонкой настройки усиления и дискриминации устройства.
    </p>
    <p>
      Встроенная система сбора данных, представленная в этой работе,
      состоит из Si-PIN-фотодиода с большой активной областью,
      предварительного усилителя заряда и формирователя.
      Эта схема может работать как альфа-спектрометр или как альфа-счетчик, в зависимости
      от электронной настройки,
      основанной на энергетическом окне альфа-излучения.
    </p>
    <h3>2 PIN-ФОТОДИОДЫ</h3>
    <p>
      Типичная PIN-фотодиодная структура показана на рис. 1.
      Она состоит из прозрачного контактного слоя р-типа с высокой степенью легирования
      сверху и слоя n-типа снизу. Наиболее важной частью PIN-фотодиода для этого устройства
      является толстый внутренний (нелегированный) слой между слоями n и p. Эта структура
      необходима для одного основного требования. Когда заряженные частицы, такие как
      альфа-лучи, ударяются о поглощающий слой PIN-фотодиода, их энергия рассеивается вдоль
      линейной дорожки, длина которой определяется типом и энергией падающих заряженных
      частиц, и электронно-дырочные пары генерируются посредством Кулоновского взаимодействия
      заряженной частицы с электронами. Количество генерируемых электронно-дырочных пар зависит
      не от типа заряженной частицы, а скорее от потери энергии (при 300 К одна
      электронно-дырочная пара генерируется в среднем на каждые 3,62 эВ).
      Поэтому потерю энергии можно оценить путем определения величины этого заряда.
    </p>
    <div class="chart-wrap">
      <img src="~/assets/imgs/ar_5_1.png" alt="" class="chart">
      <p>
        Рисунок 1 – Базовая схема структуры PIN-фотодиодов
      </p>
    </div>
    <p>
      Электронно-дырочные пары расходятся, и когда неосновные носители достигают
      соединения между двумя слоями, они ускоряются электрическим полем. Если две
      стороны электрически соединены, через соединение протекает ток. Если созданные
      миноритарные несущие этой области рекомбинируют с насыпными носителями этой области
      до достижения поля соединения,
      несущие теряются, и внешний ток не протекает.
    </p>
    <p>
      Один из наиболее важных аспектов в устройствах обнаружения связан с
      шумовыми характеристиками фотодиода. Два параметра имеют основной вклад в
      уровень шума: ток утечки и емкость фотодиода. Наличие собственного слоя снижает
      как ток утечки, так и емкость соединения, поэтому можно добиться хорошей разрешающей
      способности даже при комнатной температуре. Итак, основными характеристиками
      PIN-фотодиода, использованного в данной работе, являются: большая активная площадь
      (10 мм х 10 мм), низкая емкость, удобство использования со
      сцинтилляторами и самопроизвольное обнаружение излучения.
    </p>
    <h3>3 PIN-ФОТОДИОДНЫ УСИЛИТЕЛЬ</h3>
    <p>
      Схема усилителя PIN фотодиодов может быть разделена на две основные части:
      схема предусилителя и формирователь (рисунок 2). Предусилитель является первой
      ступенью фотодиодного усилителя, который является наиболее важным. Была использована
      интегрирующая схема, которая преобразует импульс тока из фотодиода PIN-кода Hamamatsu
      S3590-09 [8] в импульс напряжения с амплитудой, пропорциональной заряду, переносимому
      импульсом тока. Этот импульс усиливается двумя очень распространенными транзисторами
      BJT-NPN (BC547) в каскаде. Эти транзисторы, однако, имеют хорошую частотную
      характеристику, отношение сигнал / шум и линейность.
    </p>
    <div class="chart-wrap">
      <img src="~/assets/imgs/ar_5_2.png" alt="" class="chart">
      <p>
        Рисунок 2 – Схема разработанного фотодиодного усилителя
      </p>
    </div>
    <p>
      Амплитуда сигнала напряжения на выходе предусилителя составляет всего
      около нескольких десятков мВ, поэтому необходимо дополнительное усиление
      и фильтрация шума. Это выполняется схемой, называемой «формирователь».
      Каждый сигнал имеет свой естественный спектр в частотной области, и к нему
      добавляется шумовой спектр. Когда используются фильтры, можно улучшить
      уровень сигнал-шум. Для каждой ступени использовались дифференциатор и
      интегратор (фильтры CR-RC) с одинаковыми постоянными времени формирования
      (≈ 0,1 с). Транзистор 2N2222 (драйвер тока) снижает выходное сопротивление
      цепи. Для достижения оптимальных шумовых характеристик системы мы экспериментально
      исследовали зависимость постоянной времени формирования. Выходные сигналы (A и B),
      полученные после каждого каскада, можно увидеть на рисунке 3, где можно сделать вывод,
      что общее усиление каскада усилителя формирования составляет около 80-ти.
      Выходной сигнал (B) может быть подан непосредственно на многоканальный анализатор,
      благодаря чему система работает как спектрометр. Чтобы использовать систему в
      качестве счетчика импульсов, цель этой работы, уровень дискриминации выходного
      сигнала должен быть установлен в потенциометре
      1 кОм перед подачей на модуль подсчета импульсов.
    </p>
    <p>
      Весь прототип (включая фотодиод) был собран внутри черного металлического корпуса,
      чтобы избежать влияния окружающего света и защитить систему
      от электрических помех и альфа-частиц окружающей среды.
    </p>
    <p>
      За исключением фотодиода, все электронные компоненты, используемые в этой схеме,
      были легко приобретены в магазинах.
      Общая стоимость прототипа, кроме фотодиода, была менее 15 долларов США.
    </p>
