      частиц, вокруг тела возникает «темная зона», свободная от аэрозоля, толщина которой зависит от разности температур тела и среды, давления газа, размера, формы тела и не зависит от
      его химического состава. Горячее тело отталкивает от себя частицы аэрозоля, это явление
      названо термофорезом и вызвано действием термофоретических сил. Это силы, действующие со стороны
      газообразной среды на находящиеся в ней равномерно нагретые тела
      (в частности частицы аэрозоля).
