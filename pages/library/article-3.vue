    <h3>Результаты разработки и исследований</h3>
    <p>
      Смотровые стекла устанавливаются в блоках излучателя и фотоприемника для защиты оптоэлектронных компоненты измерителя от
      пыли. При эксплуатации они должны обладать малой степенью запыления, чтобы не искажались результаты измерений.
      Возможны два пути защиты стекол: существенно уменьшить
      их степень запыления или вводить автоматическую коррекцию в результат измерений для
      исключения влияния их запыленности. Наиболее простым и эффективным способом автоматической коррекции
      результатов измерений концентрации газа, является использование
      двухканальной схемы измерителя [1 – 3]. При проведении лабораторных испытаний опытного образца измерителя с
      аппаратной реализации способа компенсации запыленности установлено, что в диапазоне изменений концентрации
      метана от 0 до 2,5 об.% абсолютное значение дополнительной погрешности измерений не превышает ±0,1 об.%, что в 2 раза меньше
      абсолютного значения основной погрешности измерений (не более ±0,2 об.%). Лабораторные
      испытания проводились при изменении концентрации пыли в диапазоне от 0 до 1 г/м3.
    </p>
    <p>
      Предлагаемый тип измерителей предназначен для использования в рудничной атмосфере шахт в качестве стационарных приборов.
      Поэтому с увеличением времени непрерывной работы в условиях повышенной влажности (значение относительной влажности достигает 100 %)
      и запыленности рудничной атмосферы (концентрация пыли более 1 г/м3) происходит запыление стекол,
      что приводит к снижению метрологической надежности измерителя.
      Для предотвращения попадания пыли на стекла широко используется их обдув чистым воздухом.
      Этот способ, не смотря на его широкое применение, имеет ряд недостатков [4]:
      <ul>
