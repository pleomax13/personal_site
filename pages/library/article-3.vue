    <h3>Общая постановка проблемы</h3>
    <p>
      Одним из основных дестабилизирующих факторов
      рудничной атмосферы, оказывающим существенное влияние на показатели точности и
      метрологической надежности оптического измерителя концентрации метана, является наличие
      угольной пыли. При эксплуатации измерителя в условиях высокой запыленности и влажности
      происходит загрязнение его оптоэлектронных компонент. Для повышения точности разрабатываемого средства
      измерения в рудничной атмосфере шахт необходимо разработать
      способ защиты стекол от запыленности оптического измерителя концентрации метана.
    </p>
    <h3>Постановка задач исследования</h3>
